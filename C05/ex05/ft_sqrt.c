int	ft_sqrt(int nb)
{
	int	x;

	x = 2;
	if (nb == 0 || nb == 2147483647)
		return (0);
	if (nb == 1)
		return (1);
	if (nb < 0)
		return (0);
	while (x < nb)
	{
		if (nb == x * x)
			return (x);
		else
			x++;
	}
	return (0);
}
