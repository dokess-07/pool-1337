int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb - 1;
	if (nb == 0 || nb == 1)
		return (1);
	if (nb > 0)
	{
		while (i > 1)
		{
			nb = i * nb;
			i--;
		}
		return (nb);
	}
	return (0);
}
