void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *b;
	*b = *a;
	*a = temp;
}
#include <stdio.h>
int main (void)
{
	int i = 5;
	int j = 6;
	ft_swap(&i,&j);
	printf("%d %d" ,i,j);
return (0);
}
